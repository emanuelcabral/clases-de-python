#Igual que en las listas, podremos borrar todos los valores de un set simplemente usando la función clear.


numeros =  {1, 2, 3, 4}
print(numeros)

#eliminamos todos los valores del set
numeros.clear()
print(numeros)
# el resultado sera un set vacio
# set()

