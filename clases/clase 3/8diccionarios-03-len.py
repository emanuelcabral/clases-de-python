#¿Se acuerdan cuando hablamos de len en listas? 
#En dict, se puede usar exactamente la misma función para poder saber la longitud de un dict, es decir, la cantidad de ítems dentro del mismo.


numeros =  {'uno': 1, 'dos': 2, 'tres': 3, 'cuatro': 4}
print(len(numeros))
# 4


