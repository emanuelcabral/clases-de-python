#Para determinar si un elemento pertenece a un set, utilizamos la palabra reservada in.

numeros =  {1, 2, 3, 4}
#con "in" lo que hacemos es indicarle si ese item se encuentra dentro de el set
print(2 in numeros)
#True - nos devuelve verdadero

#Con "not in" le consultamos si el numero 2 no es parte del set.
print(2 not in numeros)
#False - nos devuelve falso porque el 2 si se encuentra dentro del set.

#Otra consulta
print(4 in numeros)
