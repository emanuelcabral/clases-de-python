from datetime import datetime
dt = datetime.now() #fecha y hora actual

print("la fecha y hora es:", dt) #fecha y hora
print("el año es:", dt.year) #año
print("el mes es:", dt.month) #mes
print("el dia es:", dt.day) #dia

print("la hora es:", dt.hour) #hora
print("el minuto es:", dt.minute) #minutos
print("el segundo es:", dt.second) #segundos
print("el microsegundo es:", dt.microsecond) #microsegundos