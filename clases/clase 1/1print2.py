###Para mostrar nuestro texto con un salto de linea se debe agregar \n para generarlo
print("Otra cadena\ncon salto de línea")

###Si quisiéramos imprimir el directorio de una carpeta

print(r"C:\nombre\directorio")

#En Python, la r antes de una cadena de texto indica que es una cadena de texto sin procesar (raw string).
#Esto significa que los caracteres de escape, como \n o \t, no se interpretan y se toman literalmente como parte de la cadena.

