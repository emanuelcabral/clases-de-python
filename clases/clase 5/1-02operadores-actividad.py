#########################################################################
#------------------------Area del Rectangulo-----------------------------
#########################################################################

# Solicitar al usuario que ingrese el lado del rectangulo
Base = float(input("Ingrese la base del rectangulo: "))
Altura = float(input("Ingrese la Altura del rectangulo: "))

# Calcular el área del rectangulo
Area = (Base * Altura)

# Mostrar el área calculada
print("El área del rectangulo es:", Area)


#########################################################################
#-------------------------Area del Triangulo-----------------------------
#########################################################################

# Solicitar al usuario que ingrese la base y la altura del triángulo
base = float(input("Ingrese la base del triángulo: "))
altura = float(input("Ingrese la altura del triángulo: "))

# Calcular el área del triángulo
area = (base * altura) / 2

# Mostrar el área calculada
print("El área del triángulo es:", area)

#########################################################################
#---------------------Perimetro del Rectangulo----------------------------
#########################################################################

# Solicitar al usuario que ingrese el lado del rectangulo
Base = float(input("Ingrese la base del rectangulo: "))
Altura = float(input("Ingrese la altura del rectangulo: "))

# Calcular el perimetro del rectangulo
perimetro = (2*Base + 2*Altura)

# Mostrar el perimetro calculado
print("El perimetro del rectangulo es:", perimetro)
