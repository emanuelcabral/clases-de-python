#El operador de igualdad sirve para preguntarle a nuestro programa si ambos operandos son iguales.
#Devolverá True si son iguales, y False si son distintos. Este operador se escribe con dos signos igual (==).
print("---Igualdad---")

a = 3
print(a)
#3
print(a == 3)
# True

