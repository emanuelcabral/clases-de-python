#tenemos la definicion

def suma(numero1, numero2):
    return numero1 + numero2

#y la llamada

r = suma(7, 5)

print(r)

#¿Por qué es importante diferenciar?


# Es importante diferenciar entre la definición y la llamada de una función porque:

# Definición: Establece lo que la función hará, especificando su comportamiento y los parámetros que requiere. Es como crear una receta.

# Llamada: Es el momento en que se usa la función con valores específicos para obtener un resultado. Es como seguir la receta para hacer un plato.

# Diferenciar estos conceptos permite estructurar y reutilizar el código de manera eficiente, facilitando su mantenimiento y comprensión.