# Esta función integrada sirve para hacer que se devuelva la misma cadena,
# pero con el primer carácter de cada palabra en mayúscula y el resto de caracteres hacerlos minúscula, 
# usando el método title(). 

# Se escribe como: string.title()


cadena = "hOLA mUNDO"
print(cadena)
print(cadena.title())
# "Hola Mundo"

print("HoLa AmIgO!".title())
# "Hola Amigo!"
